


CREATE TABLE IF NOT EXISTS APP_USER (
	user_id BIGINT NOT NULL AUTO_INCREMENT,
	
	user_email VARCHAR(255) DEFAULT NULL ,
	user_password VARCHAR(255) DEFAULT NULL,	
	user_nickname VARCHAR(255) DEFAULT NULL,
	
	user_sex enum('MEN','WOMEN') DEFAULT NULL,
	USER_STATUS enum('SING_UP','VALID','SUSPENDED') DEFAULT NULL,
	user_birthday TIMESTAMP DEFAULT NULL,
	 
	CREATION_DATE TIMESTAMP  NOT NULL,
	UPDATING_DATE TIMESTAMP  NOT NULL,
	DELETING_DATE TIMESTAMP,
	PRIMARY KEY(user_id)
);


CREATE TABLE IF NOT EXISTS APP_ROLE (
	ROLE_ID int(11) NOT NULL AUTO_INCREMENT,
	user_id varchar(45) NOT NULL,
	ROLE varchar(45) DEFAULT 'WEB_USER',
  	CREATION_DATE TIMESTAMP  NOT NULL,
	UPDATING_DATE TIMESTAMP  NOT NULL,
  	DELETING_DATE TIMESTAMP,
  PRIMARY KEY (ROLE_ID),
  UNIQUE KEY uni_username_role (ROLE,user_id),
  CONSTRAINT fk_user FOREIGN KEY (user_id) REFERENCES APP_USER (user_id));
